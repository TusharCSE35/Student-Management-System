    private void jButton_AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_AddActionPerformed
        // TODO add your handling code here:
        if(jTextField_UserName.getText().length()==0){
            JOptionPane.showMessageDialog(this, "Please Type the User name");
        }
        else if(jPasswordField_Pass.getText().length()==0){
            JOptionPane.showMessageDialog(this, "Please Type the Password");
        }
        else if(jPasswordField_Pass.getText().equals(jPasswordField_CPass.getText())==false){
            JOptionPane.showMessageDialog(this, "Password Not Matched");
        }
        else{
            try {
                String username = jTextField_UserName.getText();
                String confirmpass = jPasswordField_CPass.getText();
                
                
                Class.forName("com.mysql.cj.jdbc.Driver");
                con = DriverManager.getConnection("jdbc:mysql://localhost/stdmgdb","root","");
                pst = con.prepareStatement("INSERT INTO `user`(`username`, `password`) VALUES (?,?)");
                pst.setString(1, username);
                pst.setString(2, confirmpass);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "User Created");
                jTextField_UserName.setText("");
                jPasswordField_Pass.setText("");
                jPasswordField_CPass.setText("");
                jTextField_UserName.requestFocus();
                
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(NewUser.class.getName()).log(Level.SEVERE, null, ex);
            }     
        }
    }//GEN-LAST:event_jButton_AddActionPerformed

    
